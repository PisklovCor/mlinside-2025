package com.cryptoagents.model.enums;

/**
 * Enum для типов торговых ордеров.
 * Определяет различные типы ордеров, которые может использовать трейдер.
 */
public enum OrderType {
    /**
     * Рыночный ордер - исполняется по текущей рыночной цене
     */
    MARKET,
    
    /**
     * Лимитный ордер - исполняется только по указанной цене или лучше
     */
    LIMIT,
    
    /**
     * Стоп-ордер - исполняется когда цена достигает указанного уровня
     */
    STOP,
    
    /**
     * Стоп-лимит ордер - комбинация стоп и лимит ордера
     */
    STOP_LIMIT,
    
    /**
     * Трейлинг-стоп ордер - стоп-ордер с динамическим уровнем
     */
    TRAILING_STOP,
    
    /**
     * Айсберг ордер - крупный ордер, разбитый на мелкие части
     */
    ICEBERG,
    
    /**
     * TWAP ордер - исполнение по времени
     */
    TWAP,
    
    /**
     * VWAP ордер - исполнение по объему
     */
    VWAP
} 