package com.cryptoagents.old.model.enums;

/**
 * Enum для торговых рекомендаций.
 * Определяет возможные действия, которые может рекомендовать трейдер.
 */
public enum ActionRecommendation {
    /**
     * Рекомендация покупки
     */
    BUY,
    
    /**
     * Рекомендация продажи
     */
    SELL,
    
    /**
     * Рекомендация удержания позиции
     */
    HOLD,
    
    /**
     * Сильная рекомендация покупки
     */
    BUY_STRONG,
    
    /**
     * Сильная рекомендация продажи
     */
    SELL_STRONG,
    
    /**
     * Рекомендация накопления
     */
    ACCUMULATE,
    
    /**
     * Рекомендация распределения
     */
    DISTRIBUTE,
    
    /**
     * Рекомендация ожидания
     */
    WAIT
} 