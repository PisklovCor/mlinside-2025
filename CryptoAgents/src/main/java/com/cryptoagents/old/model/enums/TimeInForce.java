package com.cryptoagents.old.model.enums;

/**
 * Enum для времени действия торговых ордеров.
 * Определяет условия, при которых ордер остается активным.
 */
public enum TimeInForce {
    /**
     * Действует до отмены - ордер остается активным до явной отмены
     */
    GTC, // Good Till Cancelled
    
    /**
     * Немедленно или отмена - ордер исполняется немедленно или отменяется
     */
    IOC, // Immediate Or Cancel
    
    /**
     * Заполнить или убить - ордер должен быть исполнен полностью или отменен
     */
    FOK, // Fill Or Kill
    
    /**
     * Действует до конца дня - ордер автоматически отменяется в конце торгового дня
     */
    DAY,
    
    /**
     * Действует до даты - ордер остается активным до указанной даты
     */
    GTD  // Good Till Date
} 